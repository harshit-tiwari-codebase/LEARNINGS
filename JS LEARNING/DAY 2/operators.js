// arithmrtic operators
let a = 10;
let b = 5;
console.log("Addition: ", a + b); // 15
console.log("Subtraction: ", a - b); // 5
console.log("Multiplication: ", a * b); // 5
console.log("Division: ", a / b); // 2
console.log("Modulus: ", a % b); // 0

// assignment operators
let c = 20;
c += 5; // c = c + 5
console.log("Addition Assignment: ", c); // 25
c -= 5; // c = c - 5
console.log("Subtraction Assignment: ", c); // 20
c *= 2; // c = c * 2
console.log("Multiplication Assignment: ", c); // 40
c /= 4; // c = c / 4
console.log("Division Assignment: ", c); // 10
c %= 3;
console.log("Modulus Assignment: ", c); // 1

// comparison operators
let x = 10;
let y = '10';
console.log("Equal to: ", x == y);
console.log("Not equal to: ", x != y);
console.log("Strict equal to: ", x === y);
console.log("Strict not equal to: ", x !== y);
console.log("Greater than: ", x > 5);
console.log("Less than: ", x < 15);
console.log("Greater than or equal to: ", x >= 10);
console.log("Less than or equal to: ", x <= 10);

// logical operators
let p = true;
let q = false;
console.log("Logical AND: ", p && q);
console.log("Logical OR: ", p || q);
console.log("Logical NOT: ", !p);











